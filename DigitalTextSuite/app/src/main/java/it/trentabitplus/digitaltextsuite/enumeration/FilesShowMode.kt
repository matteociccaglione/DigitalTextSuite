package it.trentabitplus.digitaltextsuite.enumeration

enum class FilesShowMode {
    /**
     * Files is shown into a StaggeredLayoutManager
     */
    BIG,

    /**
     * Files is shown into a LinearLayoutManager
     */
    SMALL,

    /**
     * Directories is shown into a StaggeredLayoutManager
     */
    DIR_BIG,

    /**
     * Directories is shown into a LinearLayoutManager
     */
    DIR_SMALL
}